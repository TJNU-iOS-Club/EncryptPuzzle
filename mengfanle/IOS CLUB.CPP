#include<iostream>
using namespace std;
int main()
{
	char c;               //定义字符变量c   c指输入的单词
	int n;                //定义整型变量n   n指要移动的顺序
	int k;                //定义整型变量k   k指单词在单词表中对应的顺序
	char nc;              //定义字符变量nc  nc指移动过位置的新单词
	cout<<"n=";
	cin>>n;
	cout<<"c=";
while ((c=getchar())!='000')  //输入句子
{if(c>='a'&&c<='z')          //如果输入为小写单词则执行下列语句
{	k=c-'a';                 //算出单词在单词表中的顺序
    k=n+k;                   //算出单词移动n位后单词在单词表中的顺序
	if(k>25) k=k%26;         //如果单词顺序多出单词表则求余算出正确顺序
	nc=k+'a';                //算出移动位置后单词对应的ASCII表位置
	cout<<nc;                //输出新单词
}
if(c>='A'&&c<='Z')           //如果输入为大写单词则执行下列语句
{  k=c-'A';                  //算出单词在单词表中的顺序
k=n+k;                       //算出单词移动n位后单词在单词表中的顺序
if(k>25) k=k%26;             //如果单词顺序多出单词表则求余算出正确顺序
nc=k+'A';                    //算出移动位置后单词对应的ASCII表位置
cout<<nc;}                   //输出新单词
if(c<'A'||(c>'Z'&&c<'a')||c>'z')    //如果输入不为大写或小写单词则执行下列语句
cout<<c;                        //输出c
}
return 0;
}
